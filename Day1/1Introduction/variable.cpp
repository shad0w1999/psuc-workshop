/* This program adds two integer values and displays the results */
#include <iostream>
using namespace std;
int main (){

    // Declare variables
    int value1, value2, sum;

    // Assign values and calculate their sum
    value1 = 50;
    value2 = 40;
    sum = value1 + value2;
    
    // Display the result
    cout<<"The sum of "<<value1<<" and "<<value2<<" is"<<sum;
    return 0;
}
